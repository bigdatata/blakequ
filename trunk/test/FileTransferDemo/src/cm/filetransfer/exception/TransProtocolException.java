package cm.filetransfer.exception;

public class TransProtocolException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8585244169306866399L;

	public TransProtocolException() {
		super();
	}

	public TransProtocolException(String message, Throwable cause) {
		super(message, cause);
	}

	public TransProtocolException(String message) {
		super(message);
	}

	public TransProtocolException(Throwable cause) {
		super(cause);
	}
	
}
