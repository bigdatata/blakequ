package com.albert.exception;

/**
 * base exception
 * @author AlbertQu
 *
 */
public class GameException extends RuntimeException {

	public GameException(){
		super();
	}
	
	public GameException(final String pMessage){
		super();
	}
	
	public GameException(final Throwable pThrowable){
		super();
	}
	
	public GameException(final String pMessage, final Throwable pThrowable){
		super();
	}
}
